	<h3>CS Dept. Retake Policy</h3>
		<ul>
			<li>
			Students are allowed only one retake of any class. 
			If a student withdraws from or fails a class a second time, 
			the student will not be allowed to register for any CS course 
			for one semester (or one term, if in spring or summer). 
			This policy does not apply to classes dropped before the add/drop deadline.
			</li>
			<li>
			After one semester (or term), a student must petition to be 
			allowed to take CS courses again.
			The petition must provide evidence of a substantive change that 
			makes success in future CS classes much more likely.
			For example, a student may have demonstrated success in technology,
			math, or science-based courses.
			The petition will be reviewed by the undergraduate advisor.
			</li>
			<li>
			Petitions for non-academic (e.g., medical or military) exceptions 
			to the one semester/term waiting period may be granted according 
