    <h3>Large Projects</h3>

    <p>A major goal of CS 240 is to ensure that all students are
    able to design, implement, test, and debug large programs. To
    this end, there are two large programming projects in CS 240
    (Family Map Server and Family Map Client). Both of these projects are large
    and require several weeks of consistent effort to complete.
    There are multiple deliverables for each project, the due dates
    for which are listed in the <a href="schedule.php">class
    schedule</a>.</p>

	<p>One of the deliverables for Family Map Server is the Design assignment.  
	The grading policy for this is as follows: 100% is given for perfect or near perfect
 	submissions that meet the requirements of the assignment.
 	50% is given for submissions that need a few changes made, and where clear effort is 
	made to meet the requirements of the assignment.  Assignments given a 50% can be 
	re-submitted with the required changes made for a grade change to 100%. 0% is given when it is clear 
	that little or no effort was made to fulfill the requirements of the assignment by the due date.  
	These submissions will be treated as if nothing was submitted.  The late policy will apply 
	for any re-submission of a design graded as a 0.</p>

    <p>Since the Family Map Client project builds on the Family Map Server project,
	the Family Map Server project must be fully completed before passing it off.
	However, it may be passed off late with a penalty.
	Since it comes at the end of the course, the Family Map Client project may be 
	passed off for partial credit (see the grading rubric for details), but
	it may not be passed off late.</p>

    <p>In addition to getting your code to "work", a significant
    part of your project grade will be based on the quality of your
    work.
	Your code and automated test cases will be graded by a TA based 
	on how well you have followed the good coding and testing practices discussed
	in class. 
	Please note that the TA will evaluate the version of the code you pass off with.
	Therefore, it is in your best interest to tidy up your code and test cases as you go 
	along. </p>

    <h3>Program Pass-Offs</h3>

    <p>All programs must be passed off with a TA in the Linux labs
    during the regularly scheduled TA hours.
    We do not do timestamp-based pass offs. </p>
    
    <p>Additionally, students will have up to 5 passoff attempts for the Server before points will be deducted.
    After your 5th failed passoff attempt you will lose 1% per attempt from your FMS Program grade.
</p>

