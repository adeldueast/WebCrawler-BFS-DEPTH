    <h3>Working Together</h3>

    <p>All assignments that you turn in must be
    your own work. You may discuss concepts, ideas, and general
    approaches with others (e.g. at a whiteboard), but when it
    comes time to create actual design documentation or source code,
    you must work alone. You may not share electronic files or
    hard-copy printouts containing designs or source code with
    other students, either as giver or receiver. You may not work
    in close synchronization with other students in a side-by-side
    fashion. Violations of this policy will result in all involved
    students failing the course and the possibility of further
    action at the university level.</p>
