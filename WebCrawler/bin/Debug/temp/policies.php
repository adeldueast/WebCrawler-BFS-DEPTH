<h3>Prerequisites</h3><p>In CS 240 we assume that you have previously taken
introductory programming courses in which you learned the basic concepts of
object-oriented programming, and that you have previously taken an introductory
data structures and algorithms course where you learned about linked lists,
binary search trees, hashing, sorting, etc. At BYU-Provo these requirements are
    filled by successfully completing CS 142 and CS 235. It is recommended that 
    you also complete CS 236 before taking CS 240.  Exceptionally well-prepared
    students may take CS 240 before CS 236, depending on their level of
    programming maturity.</p>
<h3>Course Objectives</h3><p>The goal of CS 240 is to help students learn a variety
of computer programming concepts and skills that are essential in developing realistic
software systems. Specifically, the objectives of the course are to: </p>

<ol>
	<li>Have experience in and be comfortable with programming large programs
	<li>Be able to apply problem solving techniques to large programs
		<ul>
		<li>Be familiar with basic object-oriented design concepts
		<li>Know and understand basic coding, testing, and debugging strategies for large programs
		</ul>
	<li>Learn the Java programming language
	<li>Learn the fundamental concepts and implementation techniques of:
		<ul>
		<li>Relational databases
		<li>Graphical user interfaces
		<li>Mobile programming
		<li>Web APIs
		</ul>
	<li>Demonstrate the aptitude and work ethic necessary to succeed in computer science
</ol>

    <h3>Class Attendance</h3><p>You are accountable for all material presented in
