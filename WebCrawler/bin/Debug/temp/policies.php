    <h3>Honor Code Standards</h3>

    <p>In keeping with the principles of the BYU Honor Code,
    students are expected to be honest in all of their academic
    work. Academic honesty means, most fundamentally, that any work
    you present as your own must in fact be your own work and not
    that of another. Violations of this principle may result in a
    failing grade in the course and additional disciplinary action
    by the university.</p>

    <p>Students are also expected to adhere to the Dress and
    Grooming Standards. Adherence demonstrates respect for yourself
    and others and ensures an effective learning and working
    environment. It is the university's expectation, and my own
    expectation in class, that each student will abide by all Honor
    Code standards. Please call the Honor Code Office at 422-2847
    if you have questions about those standards.</p>

    <h3>Harassment</h3>

    <p>Harassment of any kind is inappropriate at BYU.
    Specifically, BYU's policy against sexual harassment extends
    not only to employees of the university but to students as
    well. If you encounter sexual harassment, gender-based
    discrimination, or other inappropriate behavior, please talk to
    your professor, contact the Equal Employment Office at 422-5895
    or 367-5689, or contact the Honor Code Office at 422-2847.</p>

    <h3>Disabilities</h3>

    <p>BYU is committed to providing reasonable accommodation to
    qualified persons with disabilities. If you have any disability
    that may adversely affect your success in this course, please
    contact the University Accessibility Center at 422-2767.
    Services deemed appropriate will be coordinated with the
    student and instructor by that office.</p>

    <h3>Children in the Classroom</h3>

    <p>The serious study of the physical and mathematical sciences
    requires uninterrupted concentration and focus in the
    classroom. Having small children in class is often a
    distraction that degrades the educational experience for the
    entire class. Please make other arrangements for child care
    rather than bringing children to class with you. If there are
    extenuating circumstances, please talk with your instructor in
    advance.</p>


