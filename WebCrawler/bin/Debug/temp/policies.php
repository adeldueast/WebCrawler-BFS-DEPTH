    <h3>Programming Exam Problems</h3>

    <p>At the beginning of the course, you will do several relatively
    small programming projects. These programs are designed to help
    you gain experience with Java and the Linux programming
    environment, and to prepare you for the Programming Exam. In
    fact, they are the same programs that you will be required to
    implement when you take the Programming Exam. The only way to
    pass the Programming Exam is to implement these programs before
    taking the exam. Each program should be completed and passed
    off with a TA on or before the due date (see the <a href=
    "schedule.php">class schedule</a> for due dates).</p>

    <p>Although these programs may be passed off after their due
    dates, you should consider yourself behind in the class if you
    are not completing the programs on time. With the pressures of
    your other classes, you may be tempted to get behind in CS240
    with the intent to "catch up" later. Experience has shown that
    you are very unlikely to "catch up" if you get behind, so don't
    let it happen. Discipline yourself and stay on schedule. The
    surest way to fail CS240 is to get behind.</p>

    <h3>Programming Exam</h3>

    <p>One of the major goals of CS 240 is to ensure that all
    students have sufficiently strong programming skills to succeed
    in upper division computer science courses. One mechanism for
    ensuring this is the Programming Exam.</p>

    <p>The exam is taken in one of the CS Dept. Linux labs and
    administered by a TA. You will be randomly assigned one of the
    Programming Exam problems and given three hours to implement a
    solution to the problem. In order to pass the exam, you must
    completely finish the program and pass it off to the TA within
    the time limit.</p>

    <p>There is no partial credit on the Programming Exam; you
    either finish, or you don't. 
      You may bring one sheet of notes to the exam, but your notes may not be project-specific
      (i.e., they may not contain any source code, pseudo-code, or design diagrams
      that are specific to the exam projects).
    The computer used to take the exam will be
    disconnected from the network, thus removing any temptation to
    access a previously implemented solution to the problem.</p>

    <p><b>You must pass the Programming Exam in order to pass
    CS240.</b> The key to passing the exam is to implement each of
    the programs ahead of time. Please note that if you do not
    implement the programs beforehand, it is highly unlikely that
    you will pass the exam. Because passing the Programming Exam is
    required to pass the class, you may take the exam multiple
    times until you pass it (see the <a href="schedule.php">class
    schedule</a> for exam dates). However, your maximum score for
    the exam will go down each time you retake it.</p>

